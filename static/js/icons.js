/* =========================================================
    JS HELPER
    Returns an SVG string ready for innerHTML.
    icon("eraser")        → 1em × 1em
    icon("eraser", "14px") → 14px × 14px
========================================================= */

function icon(name, size, transform) {
    const def = ICONS[name];
    if (!def) { console.warn(`icon: unknown icon "${name}"`); return ""; }
    const sz = size || "1em";
    const style = transform
        ? `display:block;flex-shrink:0;transform:${transform}`
        : `display:block;flex-shrink:0`;
    const fillRule = def.fillRule ? ` fill-rule="${def.fillRule}"` : "";

    // Stroke-style icons render as outline rather than filled silhouette
    const pathExtra = def.stroke
        ? ` fill="none" stroke="currentColor" stroke-width="${def.strokeWidth || 32}" stroke-linecap="round" stroke-linejoin="round"`
        : "";
    const svgFill = def.stroke ? "none" : "currentColor";

    const inner = def.g_transform
        ? `<g transform="${def.g_transform}"><path d="${def.d}"${fillRule}${pathExtra}/></g>`
        : `<path d="${def.d}"${fillRule}${pathExtra}/>`;
    return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${def.viewBox}" width="${sz}" height="${sz}" fill="${svgFill}" style="${style}">${inner}</svg>`;
}

