/* =========================================================
    JS HELPER
    Returns an SVG string ready for innerHTML.
    icon("eraser")        → 1em × 1em
    icon("eraser", "14px") → 14px × 14px
========================================================= */

function icon(name, size, transform) {
    const def = ICONS[name];
    if (!def) { console.warn(`icon: unknown icon "${name}"`); return ""; }
    const sz = size || "1em";
    const style = transform
        ? `display:block;flex-shrink:0;transform:${transform}`
        : `display:block;flex-shrink:0`;
    const fillRule = def.fillRule ? ` fill-rule="${def.fillRule}"` : "";
    return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${def.viewBox}" width="${sz}" height="${sz}" fill="currentColor" style="${style}"><path d="${def.d}"${fillRule}/></svg>`;
}

